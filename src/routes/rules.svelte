<div class="centered">
	<h1 class="center">Rules</h1>

	<h2>Setup</h2>
	<p>
		<i>
			<b>Playing Area:</b>
		</i>
		Smugglers Cove is best played outside, in a large area. Obstructions or small buildings within the playing field are recommended.
	</p>
	<p>
		<i>
			<b>Bases:</b>
		</i>
		Three object within the playing field are needed. One for each team (yes, this game has three teams). The bases should not be close
		to each other, and should be about the same distance apart.
	</p>
	<p>
		<i>
			<b>Flags:</b>
		</i>
		Three types of flags are required (one type for each team). Each have on them two flags, one in a visibile location, one in a hidden
		location. As the flags define which players are on which team, any player without a visible flag on him/her, is not allowed to play
		until at least one of their flags is again made visibile.
	</p>
	<div class="note">
		NOTE: We often use bandanas for the robbers, small white hand towels for the smugglers, and black socks for the cops.
	</div>

	<div class="pad20" />

	<p>
		<i>
			<b>Creating Teams:</b>
		</i>
		The easiest way to do this is to use the
		<a href="#team-generator">team generator.</a>
		The generator will divide the players into teams and assign each player a buddy (more on this later). Do not tell anyone who your
		buddy is.
	</p>
	<p>
		<i>
			<b>Positioning:</b>
		</i>
		After each player has recived and positioned their flags, all players can then go to their team's base. The game is ready to begin.
	</p>

	<div class="pad20" />

	<h2>Gameplay</h2>
	<p>
		The game proceeds with all of the Robbers chasing the Smugglers, the Cops chasing the Robbers, and the Smuglers chasing the Cops.
		There are a few ways that a catch can conducted, either simply tag the player that you are trying to catch, or pull out their flag
		and drop it on the ground. The way players catch each other must be consistient throughout the game.
	</p>

	<p>
		Once a player catches another player, the player that was caught must go to base of the team of the player that caught him. If he
		was caught by pulling a flag, the player that caught him should have dropped the flag on the ground. The player that was caught must
		then pick up their flag and hold it above his head* as he walks (or runs) to the base of the team of the player that caught him. If,
		however, the player was caught by tagging the player that caught him must escort the him to his base. The player that is escorting
		the player caught player to his base is no allowed to be caught by a player from the team that is trying to catch him. If the escort
		is abandonded or never started, the caught player is free to go.
	</p>

	<p>
		Once a player is in another team's base that player is not allowed to leave until one of their teamates touches the base (or a
		player within that base). When this happens, all players in the base are both free to leave, and imidatly enter into the state that
		they were before they were caught. Players that were tagged by the pulling out of a flag that have not put their flags back in are
		not allowed to be freed.
	</p>

	<div class="note">
		Remember: Only Robbers can catch Smugglers, only Cops can catch Robbers, and only Smugglers can catch Cops. Only players on the same
		team can free eachother from a base.
	</div>

	<div class="pad20" />

	<h2>Winning</h2>

	<p>The first team to be completly caught (no one left to free the caught players) gets 3rd place.</p>
	<p>The team that caught the team that got third place gets 1st place.</p>
	<p>The other team gets 2nd place.</p>
	<p>Optionally the teams that got 3rd and 1st can team up and catch the team that got 2nd.</p>

